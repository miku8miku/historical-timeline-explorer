// 获取事件所属时期
export const getEraByYear = (year) => {
  for (const [eraKey, era] of Object.entries(eras)) {
    if (year >= era.start && year <= era.end) {
      return { key: eraKey, ...era };
    }
  }
  return null;
};

// 按时间范围过滤事件
export const getEventsInTimeRange = (events, startYear, endYear) => {
  return events.filter(event => event.year >= startYear && event.year <= endYear);
};